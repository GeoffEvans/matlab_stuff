            aodDirectionVectors = {[1;0], [0;1]};
            aodDirectionVectors = [aodDirectionVectors, cellfun(@(x) x*(-1), aodDirectionVectors,'UniformOutput', false)];
            aodL = [5e-12 * ones(1,3), focalLength];
